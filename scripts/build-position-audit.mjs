import { mkdir, readFile, writeFile } from "node:fs/promises";

const BASE_URL = "https://www.rugbyleagueproject.org";
const CACHE_DIR = ".rlp-position-cache";
const POSITION_ORDER = ["fullback", "wing", "centre", "half", "edge", "middle", "lock", "hooker"];
const POSITION_MAP = {
  Fullback: "fullback",
  Wing: "wing",
  Centre: "centre",
  "Five-eighth": "half",
  Halfback: "half",
  "Front Row": "middle",
  Prop: "middle",
  Hooker: "hooker",
  "Second Row": "edge",
  Lock: "lock"
};
const MIN_SECONDARY_APPS = 8;
const MIN_SECONDARY_SHARE = 0.25;
const STRONG_SECONDARY_APPS = 24;
const FALLBACK_STRONG_SECONDARY_WEIGHT = 90;
const FALLBACK_STRONG_SECONDARY_SHARE = 0.1;
const MIN_CAREER_COUNT_APPS = 24;
const CONCURRENCY = 2;
const REQUEST_DELAY_MS = 900;
const MAX_FETCH_ATTEMPTS = 4;
const CACHE_ONLY = process.argv.includes("--cache-only");

async function main() {
  await mkdir(CACHE_DIR, { recursive: true });
  const rows = await readGeneratedRows();
  const careers = buildCareers(rows);
  const limitArg = process.argv.find((arg) => arg.startsWith("--limit="));
  const limit = limitArg ? Number(limitArg.split("=")[1]) : careers.length;
  const selectedCareers = careers.slice(0, Number.isFinite(limit) ? limit : careers.length);
  const audited = [];
  let completed = 0;

  await runPool(selectedCareers, CONCURRENCY, async (career) => {
    const audit = await auditCareer(career);
    audited.push(audit);
    completed += 1;

    if (completed % 50 === 0 || completed === selectedCareers.length) {
      console.log(`Audited ${completed}/${selectedCareers.length}`);
    }
  });

  audited.sort((a, b) => a.name.localeCompare(b.name) || a.careerId.localeCompare(b.careerId));
  const byCareerId = Object.fromEntries(audited.map((item) => [item.careerId, toPayloadItem(item)]));
  const byNameKey = {};

  for (const item of audited) {
    const existing = byNameKey[item.nameKey];
    if (!existing || item.totalCareerApps > existing.totalCareerApps) {
      byNameKey[item.nameKey] = toPayloadItem(item);
    }
  }

  const payload = {
    meta: {
      generatedAt: new Date().toISOString(),
      source: "Rugby League Project career position pages cross-checked against the generated NRL season-team position data.",
      rule: `Primary position always kept. Extra positions kept when they have at least ${MIN_SECONDARY_APPS} appearances and either at least ${Math.round(MIN_SECONDARY_SHARE * 100)}% of primary appearances, at least ${STRONG_SECONDARY_APPS} verified career-page appearances, or strong fallback season evidence of ${FALLBACK_STRONG_SECONDARY_WEIGHT}+ weighted starts and ${Math.round(FALLBACK_STRONG_SECONDARY_SHARE * 100)}% of primary.`,
      auditedCareers: audited.length,
      careerPageCareers: audited.filter((item) => item.fetchStatus === "fetched" && item.totalCareerApps >= MIN_CAREER_COUNT_APPS).length,
      seasonEvidenceCareers: audited.filter((item) => item.fetchStatus !== "fetched" || item.totalCareerApps < MIN_CAREER_COUNT_APPS).length,
      generatedPlayerSeasons: rows.length
    },
    byCareerId,
    byNameKey
  };

  await writeFile(
    "player-position-audit.js",
    `// Generated by scripts/build-position-audit.mjs\nwindow.NRL_PLAYER_POSITION_AUDIT = ${JSON.stringify(payload)};\n`,
    "utf8"
  );
  console.log(`Wrote ${audited.length} career position audits to player-position-audit.js`);
}

async function readGeneratedRows() {
  const text = await readFile("player-data.js", "utf8");
  const match = text.match(/window\.NRL_GENERATED_PLAYER_SEASONS\s*=\s*(\[[\s\S]*?\]);/);
  if (!match) throw new Error("Could not find NRL_GENERATED_PLAYER_SEASONS in player-data.js");
  return JSON.parse(match[1]);
}

function buildCareers(rows) {
  const map = new Map();

  for (const row of rows) {
    const playerId = playerIdFromCareerId(row.careerId);
    if (!playerId) continue;

    if (!map.has(row.careerId)) {
      map.set(row.careerId, {
        careerId: row.careerId,
        playerId,
        slug: slugFromCareerId(row.careerId),
        name: row.name,
        nameKey: normalizeNameForKey(row.name),
        seasonEvidence: new Map()
      });
    }

    const career = map.get(row.careerId);
    for (const [index, position] of (row.positions || []).entries()) {
      if (!POSITION_ORDER.includes(position)) continue;
      const weight = Math.max(1, row.starts || 1) * (index === 0 ? 3 : 1);
      career.seasonEvidence.set(position, (career.seasonEvidence.get(position) || 0) + weight);
    }
  }

  return [...map.values()].sort((a, b) => Number(a.playerId) - Number(b.playerId));
}

async function auditCareer(career) {
  const sourceUrl = `${BASE_URL}/players/${career.slug}/positions.html`;
  let html = "";
  let fetchStatus = "fetched";

  try {
    html = await fetchCached(`${career.playerId}-${career.slug}`, sourceUrl);
  } catch (error) {
    fetchStatus = `fallback: ${error.message}`;
  }

  const careerCounts = parseCareerPositionCounts(html);
  const seasonCounts = Object.fromEntries([...career.seasonEvidence.entries()].sort(sortPositionEntries));
  const totalCareerApps = Object.values(careerCounts).reduce((sum, value) => sum + value, 0);
  const regularPositions = chooseRegularPositions(careerCounts, seasonCounts);

  return {
    ...career,
    sourceUrl,
    counts: careerCounts,
    seasonCounts,
    positions: regularPositions,
    totalCareerApps,
    fetchStatus
  };
}

function parseCareerPositionCounts(html) {
  const tableStart = html.indexOf("<h3>Positions</h3>");
  if (tableStart < 0) return {};

  const sectionStart = html.indexOf("<h3>Competitions</h3>", tableStart);
  if (sectionStart < 0) return {};

  const nextSection = html.indexOf("<h3>International</h3>", sectionStart);
  const section = html.slice(sectionStart, nextSection > sectionStart ? nextSection : html.indexOf("</table>", sectionStart));
  const counts = new Map();

  for (const match of section.matchAll(/<tr>\s*<td class="text">([\s\S]*?)<\/td>\s*<td class="n">([\s\S]*?)<\/td>[\s\S]*?<\/tr>/g)) {
    const rawPosition = cleanText(match[1]);
    const position = POSITION_MAP[rawPosition];
    if (!position) continue;

    const apps = numberFromCell(match[2]);
    if (!apps) continue;
    counts.set(position, (counts.get(position) || 0) + apps);
  }

  return Object.fromEntries([...counts.entries()].sort(sortPositionEntries));
}

function chooseRegularPositions(careerCounts, seasonCounts) {
  const careerEntries = Object.entries(careerCounts).filter(([, count]) => count > 0);
  const careerTotal = careerEntries.reduce((sum, [, count]) => sum + count, 0);
  const usesCareerCounts = careerTotal >= MIN_CAREER_COUNT_APPS;
  const sourceEntries = usesCareerCounts ? careerEntries : Object.entries(seasonCounts);
  const sorted = sourceEntries.sort((a, b) => b[1] - a[1] || POSITION_ORDER.indexOf(a[0]) - POSITION_ORDER.indexOf(b[0]));

  if (!sorted.length) return [];

  const primaryCount = sorted[0][1];
  const positions = sorted
    .filter(([, count], index) => {
      if (index === 0) return true;
      const strongEnough = usesCareerCounts
        ? count >= STRONG_SECONDARY_APPS
        : count >= FALLBACK_STRONG_SECONDARY_WEIGHT && count >= primaryCount * FALLBACK_STRONG_SECONDARY_SHARE;
      return count >= MIN_SECONDARY_APPS && (count >= primaryCount * MIN_SECONDARY_SHARE || strongEnough);
    })
    .map(([position]) => position);

  return positions;
}

async function fetchCached(cacheKey, url) {
  const path = `${CACHE_DIR}/${cacheKey}.html`;

  try {
    return await readFile(path, "utf8");
  } catch {
    // Cache miss.
  }

  if (CACHE_ONLY) {
    throw new Error(`No cached position page for ${url}`);
  }

  let response;

  for (let attempt = 1; attempt <= MAX_FETCH_ATTEMPTS; attempt += 1) {
    await sleep(REQUEST_DELAY_MS * attempt);
    response = await fetch(url, {
      headers: {
        "user-agent": "NRL Invincible position audit prototype"
      }
    });

    if (response.ok) break;
    if (response.status !== 429 || attempt === MAX_FETCH_ATTEMPTS) break;
    await sleep(5000 * attempt);
  }

  if (!response.ok) {
    throw new Error(`Failed ${url}: ${response.status}`);
  }

  const text = await response.text();
  await writeFile(path, text, "utf8");
  return text;
}

async function runPool(items, concurrency, worker) {
  let index = 0;
  const workers = Array.from({ length: concurrency }, async () => {
    while (index < items.length) {
      const item = items[index];
      index += 1;
      await worker(item);
    }
  });

  await Promise.all(workers);
}

function toPayloadItem(item) {
  return {
    name: item.name,
    playerId: item.playerId,
    positions: item.positions,
    counts: item.counts,
    seasonCounts: item.seasonCounts,
    totalCareerApps: item.totalCareerApps,
    fetchStatus: item.fetchStatus,
    sourceUrl: item.sourceUrl
  };
}

function playerIdFromCareerId(careerId) {
  return String(careerId || "").match(/-(\d+)$/)?.[1] || "";
}

function slugFromCareerId(careerId) {
  return String(careerId || "").replace(/-\d+$/, "");
}

function sortPositionEntries(a, b) {
  return POSITION_ORDER.indexOf(a[0]) - POSITION_ORDER.indexOf(b[0]);
}

function numberFromCell(value) {
  const cleaned = cleanText(value).replace(/,/g, "");
  if (!cleaned || cleaned === "-") return 0;
  return Number(cleaned) || 0;
}

function cleanText(value) {
  return String(value || "")
    .replace(/<[^>]*>/g, "")
    .replace(/&amp;/g, "&")
    .replace(/&#039;/g, "'")
    .replace(/&quot;/g, '"')
    .replace(/&nbsp;/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function normalizeNameForKey(value) {
  return String(value || "").toLowerCase().replace(/[^a-z0-9]+/g, "");
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
